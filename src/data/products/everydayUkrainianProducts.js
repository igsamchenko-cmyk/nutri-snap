const makeProduct = ([name, brand, supermarket, category, calories, protein, fat, carbs, weight, icon, aliases = "", ingredients = ""]) => {
  const product = {
    id: `ua-everyday-${name}-${brand}`.toLowerCase().replace(/[^\p{L}\p{N}]+/gu, "-").replace(/^-+|-+$/g, ""),
    name,
    brand,
    supermarket,
    category,
    calories,
    protein,
    fat,
    carbs,
    weight,
    icon,
    aliases: String(aliases).split(";").map(item => item.trim()).filter(Boolean),
    ingredients,
    confidence: 93,
    source: "ua-everyday",
    sourceLabel: "База UA+"
  };

  product.searchText = [
    product.name,
    product.brand,
    product.supermarket,
    product.category,
    product.aliases.join(" ")
  ].filter(Boolean).join(" ").toLowerCase();

  return product;
};


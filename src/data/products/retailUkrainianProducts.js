const makeProduct = ([name, brand, supermarket, category, calories, protein, fat, carbs, weight, icon, aliases = "", ingredients = ""]) => {
  const product = {
    id: `ua-retail-${supermarket}-${brand}-${name}`.toLowerCase().replace(/[^\p{L}\p{N}]+/gu, "-").replace(/^-+|-+$/g, ""),
    name,
    brand,
    supermarket,
    category,
    calories,
    protein,
    fat,
    carbs,
    weight,
    icon,
    aliases: [
      brand,
      supermarket,
      ...String(aliases).split(";").map(item => item.trim()).filter(Boolean)
    ].filter(Boolean),
    ingredients,
    confidence: 92,
    source: "ua-retail",
    sourceLabel: supermarket || "База UA+"
  };

  product.searchText = [
    product.name,
    product.brand,
    product.supermarket,
    product.category,
    product.aliases.join(" ")
  ].filter(Boolean).join(" ").toLowerCase();

  return product;
};


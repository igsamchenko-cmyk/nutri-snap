const makeProduct = ([name, brand, supermarket, category, calories, protein, fat, carbs, weight, icon, aliases = "", ingredients = ""]) => {
  const product = {
    id: `ua-plus-${name}-${brand}`.toLowerCase().replace(/[^\p{L}\p{N}]+/gu, "-").replace(/^-+|-+$/g, ""),
    name,
    brand,
    supermarket,
    category,
    calories,
    protein,
    fat,
    carbs,
    weight,
    icon,
    aliases: String(aliases).split(";").map(item => item.trim()).filter(Boolean),
    ingredients,
    confidence: 94,
    source: "ua-expanded",
    sourceLabel: "База UA+"
  };

  product.searchText = [
    product.name,
    product.brand,
    product.supermarket,
    product.category,
    product.aliases.join(" ")
  ].filter(Boolean).join(" ").toLowerCase();

  return product;
};

